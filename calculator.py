num1 = int(input("Please enter the first number: "))
num2 = int(input("Please enter the second number: "))
operation = input("Please choose the operation(+, -, *, /): ")

if operation == "+":
    print(num1 + num2)
if operation == "-":
    print(num1 - num2)
if operation == "*":
    print(num1 * num2)
if operation == "/":
    print(num1 / num2)